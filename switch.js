let fruits = prompt("Enter fruits name : ");
console.log(fruits);

switch (fruits) {
    case 'apple': console.log("this is apple");
        break;
    case 'orange': console.log("this is orange");
    break;
    case 'pine': console.log("this is pine");
        break;
    case 'banana': console.log("this is banana");
    break;

    default: console.log("Something wrong.");
        break;
}